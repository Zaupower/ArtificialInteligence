public class Knapsack {
    int valor;
    int weight;

    public Knapsack(int valor, int weight) {
        this.valor = valor;
        this.weight = weight;
    }

    public int getValor() {
        return valor;
    }

    public void setValor(int valor) {
        this.valor = valor;
    }

    public int getWeight() {
        return weight;
    }

    public void setWeight(int weight) {
        this.weight = weight;
    }
}
